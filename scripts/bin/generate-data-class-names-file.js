#!/usr/bin/env node

const PATH = require('path');

const RELATIVE_INPUT_FILE = 'docs/_data/variables.json';

const INPUT_FILE = PATH.resolve(__dirname, `../../${RELATIVE_INPUT_FILE}`);

const INPUT = require(INPUT_FILE);

const output = {};

output._TITLE = `THIS FILE IS AUTOGENERATED -- DO NOT MAKE CHANGES!`;
output._INFO = `Created using ./scripts/bin/compile-scss-variables.sh from ./${RELATIVE_INPUT_FILE}`;

Object.keys(INPUT).forEach(key => {

    let value = INPUT[key];

    if ( !value.startsWith('.') ) {
        return;
    }

    value = value.substr(1);

    output[key] = value;

});

console.log(JSON.stringify(output, null, 2));
